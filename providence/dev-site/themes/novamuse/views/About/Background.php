	<h1>Background</h1>
	<div class="textContent">
		<div>
			Over fifty community museums from across Nova Scotia are providing access to their collections in a way never before possible. Many of these museums are only open during the summer months and are run by volunteers. Others are open year-round and rely on a combination of staff and volunteers to operate. They all have one thing in common – people who are passionate about Nova Scotia’s history. 
		</div>
		<div>
			It is this passion that has made NovaMuse a reality. In 2002 a group of 18 community museums recognized the need for standardizing their collections practices. They started by using the same database system and bulk purchasing computer equipment, and became known as the Passage Project. Museum staff and volunteers were trained individually and in workshops, and the community museums began digitizing Nova Scotia’s material culture. Recognized as a groundbreaking initiative, in 2006 the Passage Project received a Canadian Museums Association Award for Outstanding Achievement in Museum Management. Over the years this group of 18 grew to over 50 heritage organizations from across the province. 
		</div>
		<div>
			Now supported by the Association of Nova Scotia Museum’s Advisory Service, participating museums recognize the value of cooperative work and a peer support network. They actively strive to improve their institutions through special projects and partnerships. Collections management documentation and database technology provides the means to better record, inventory, access and research information about collections. More than a basic accountability tool, the 2011 migration to a new web-based database system provided the information platform required to support a public website with robust search and sharing capacity. And NovaMuse was born. 
		</div>
		<div>
			In 2014 a database of Nova Scotian industrial history was integrated with the museum collections data. Significant research contributions were made by the Nova Scotia Museum of Industry, Annapolis Valley Macdonald Museum, Beaton Institute, Cumberland County Museum, Dartmouth Heritage Museum, Kings County Museum, West Hants Historical Museum, Yarmouth County Museum, and Association of Nova Scotia Museums.
		</div>
		<div>
			Contributors are at different stages in the documentation process. Some have been working in databases for years and so have their entire collection documented and photographed, while others have just started the process and are working at building their database. Institutional priorities and differing levels of resources mean that some contributors are more active than others. This work is a never-ending process; research and information received from the public can contribute to existing records. NovaMuse will be updated daily, revealing more objects and images as files are updated.
