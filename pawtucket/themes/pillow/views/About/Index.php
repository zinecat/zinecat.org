		<h2>About the Collection</h2>
		
		<p>The Jacob’s Pillow Archives document dance companies and artists who have played an important role in the rich history of our Festival and School, with special emphasis on Ted Shawn, Denishawn, and Ted Shawn’s Men Dancers. The collection includes correspondence, photographs, performance programs, board minutes, books, costumes, posters, films, videos, sound recordings, and scrapbooks. In our Reading Room, the public has access to our extensive book and periodicals collection – also with a strong focus on dance and the performing arts.</p>
		<p>The Archives comprise one aspect of the Pillow’s Preservation Program, which documents the ongoing activities of the Festival (principally on HD digital video), schedules films and talks, and curates exhibits exploring various aspects of dance. The center for many of the Pillow’s preservation activities is Blake’s Barn, an 18th-century structure that was donated by Marge Champion in 1990, which was later relocated from its original location in Stockbridge and reconfigured for public events.</p>
		<p>In 2015, the Archives underwent a major renovation, which increased physical space (both public and storage) and technology in the new Norton Owen Reading Room, including adding more viewing stations and exhibition space.</p>
	
		<h2>Visit the Archives</h2>
		
		<p><i>On-Site Access</i></p>
		<p>During the summer season, the Norton Owen Reading Room is open daily from noon until final curtain. Everyone is welcome and encouraged to come explore – including Pillow students, faculty, visiting artists, long-time patrons or first time visitors - and there is no appointment necessary. Simply stop by Blake’s Barn!</p>
		<p>Visitors can peruse our print collection of over 2500 books and periodicals, watch our award-winning Pillow documentary Never Stand Still on our big screen, and have immediate access to over 6000 performance and other Pillow related video documentation. Full versions of nearly every performance from the past 35 years are available here for viewing, along with talks, rehearsals, classes, interviews, and films dating from the earliest days of the Pillow.</p>
		
		<p><i>Events</i></p>
		<p>The Archives provide many unique opportunities to attend and participate in moderated interviews and discussions related to the dance field with artists and historians. We also offer film screenings and book signings during the festival season. These are also documented by our video team and added to our permanent collection.</p>
		
		<p><i>Exhibits</i></p>
