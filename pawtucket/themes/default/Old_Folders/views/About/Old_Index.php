		<h6>Welcome to the Zine Union Catalog!</h6>
		<br><p> Zinecat.org is a “union catalog” dedicated to zines! A union catalog is a resource where libraries can share cataloging and holdings information. The Zine Union Catalog (ZUC) enables researchers to discover zine holdings by searching a single catalog, as well as making it possible for librarians to copy catalog records and facilitates lending across libraries. ZUC serves educators, researchers, librarians, archivists, and anyone in the general public with an interest in zines.</p> 
<p> ZUC is a product of the CUNY Graduate Center's Digital Praxis which provides essential resources to support the project. Special thanks to <a href="http://qzap.org"> QZAP.org </a>and many others who helped make the ZUC possible.</p>
